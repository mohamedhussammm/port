function showPage(pageId) {
    // Hide all pages
    const pages = document.querySelectorAll('.page');
    pages.forEach(page => page.classList.remove('active'));
    
    // Show selected page
    document.getElementById(pageId).classList.add('active');
    
    // Update navigation
    const navLinks = document.querySelectorAll('nav a');
    navLinks.forEach(link => link.classList.remove('active'));
    if(event && event.target) {
        event.target.classList.add('active');
    }
    
    // Scroll to top smoothly
    window.scrollTo({
        top: 0,
        behavior: 'smooth'
    });
}

function filterProjects(category) {
    const projects = document.querySelectorAll('.project-card');
    const buttons = document.querySelectorAll('.filter-btn');
    
    // Update active button
    buttons.forEach(btn => btn.classList.remove('active'));
    if(event && event.target) {
        event.target.classList.add('active');
    }
    
    // Filter projects
    projects.forEach(project => {
        if (category === 'all' || project.classList.contains(category)) {
            project.style.display = 'block';
            project.style.animation = 'fadeIn 0.5s ease-out forwards';
        } else {
            project.style.display = 'none';
        }
    });
}

function showProjectDetails(projectId) {
    // Hide all pages
    const pages = document.querySelectorAll('.page');
    pages.forEach(page => page.classList.remove('active'));
    
    // Show project details page
    document.getElementById('project-details').classList.add('active');
    
    // Update project details content
    updateProjectDetails(projectId);
    
    // Scroll to top
    window.scrollTo({
        top: 0,
        behavior: 'smooth'
    });
}

function updateProjectDetails(projectId) {
    const project = projectData[projectId];
    if (project) {
        document.getElementById('project-title').textContent = project.title;
        document.getElementById('project-tech').textContent = project.tech;
        document.getElementById('project-description').textContent = project.description;
        
        // Update project images dynamically
        const imagesGrid = document.getElementById('project-images-grid');
        imagesGrid.innerHTML = ''; // Clear existing images
        
        if (project.images && project.images.length > 0) {
            project.images.forEach((imgSrc, index) => {
                const imgContainer = document.createElement('div');
                imgContainer.className = 'project-detail-img-container';
                imgContainer.style.cursor = 'zoom-in';
                imgContainer.onclick = () => openLightbox(imgSrc);
                
                const img = document.createElement('img');
                img.src = imgSrc;
                img.alt = `${project.title} Image ${index + 1}`;
                
                imgContainer.appendChild(img);
                imagesGrid.appendChild(imgContainer);
            });
        }
        
        const featuresList = document.getElementById('project-features');
        featuresList.innerHTML = '';
        project.features.forEach(feature => {
            const li = document.createElement('li');
            li.textContent = feature;
            featuresList.appendChild(li);
        });
        
        const githubLink = document.getElementById('github-link');
        if(githubLink) {
            githubLink.href = project.github;
        }
    }
}

function goBackToPortfolio() {
    showPage('portfolio');
}


// Lightbox functionality
function openLightbox(imageSrc) {
    const lightbox = document.getElementById('lightbox');
    const lightboxImg = document.getElementById('lightbox-img');
    
    if (lightbox && lightboxImg) {
        lightboxImg.src = imageSrc;
        lightbox.classList.add('active');
        document.body.style.overflow = 'hidden'; // Prevent background scrolling
    }
}

function closeLightbox() {
    const lightbox = document.getElementById('lightbox');
    if (lightbox) {
        lightbox.classList.remove('active');
        document.body.style.overflow = 'auto'; // Restore scrolling
    }
}